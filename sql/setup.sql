DROP TABLE IF EXISTS cars;
DROP TABLE IF EXISTS movies;

CREATE TABLE cars (
    id BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    make  TEXT NOT NULL,
    color TEXT NOT NULL,
    year INT NOT NULL
);

CREATE TABLE movies (
    id BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    rating INT NOT NULL,
    director TEXT NOT NULL
);